class Player():
    hp = 0
    damage = 0
    talent = 0
    luck = 0
    level = 0
    exp = 0
    exp_required = 0
    coins = 0
    points = 0
    attack_delay = 0

    def __init__ (self, hp, dm, tl, lk, lvl, exp, exp_req, cs, ps, delay):
        self.hp = hp
        self.damage = dm
        self.talent = tl
        self.luck = lk
        self.level = lvl
        self.exp = exp
        self.exp_required = exp_req
        self.coins = cs
        self.points = ps
        self.attack_delay

    def recieve_damage(self, damage):
        self.hp -= damage

    def promote_to_next_level(self):
        self.level += 1
        self.exp = self.exp - self.exp_required
        self.exp_required *= 1.1
        self.points += 1

    def add_char_bonus(self):
        self.hp *= 1.5
        self.damage *= 1.3
        self.luck *= 1.2

    def remove_char_bonus(self):
        pass

